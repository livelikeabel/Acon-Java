package ch2.test;

public class String3Equals {

	public static void main(String[] args) {
		String str3 = "hi";
		String str5 = "abel";
		System.out.println();
		System.out.println();
		System.out.println();
		System.out.println(str3 == str5);
		System.out.println(str3.equals(str5));
		System.out.println(str3.equalsIgnoreCase(str5));
		System.out.println("=============");
	}
}
