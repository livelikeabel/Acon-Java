package ch2.test;

public class PrimitiveChar {

	public static void main(String[] args) {
		char charVar, charVar2;
		charVar = 'Z';
		charVar2 = 'วั';
		
		System.out.println("charVar : " + charVar);
		System.out.println("charVar2 : " + charVar2);
		
		
		char asciiVar1 = 64;
		char asciiVar2 = 65;
		char asciiVar3 = 97;
		
		System.out.println("asciiVar1 : " + asciiVar1);
		System.out.println("asciiVar2 : " + asciiVar2);
		System.out.println("asciiVar3 : " + asciiVar3);
	}
}
